    private void Table1() {
    ArrayList<Laboratoire> lis =gestLabo.getAllLaboratoire();
        model =(DefaultTableModel)TableLabo.getModel();
        Object[] ob = new Object[6];
        for (int i = 0 ; i<lis.size() ; i++){
            ob[0] = lis.get(i).getId_laboratoire();
            ob[1] = lis.get(i).getNom_labo();
            ob[2] = lis.get(i).getResponsable();
            ob[3] = lis.get(i).getEmail();

            model.addRow(ob);    }
    }
    
