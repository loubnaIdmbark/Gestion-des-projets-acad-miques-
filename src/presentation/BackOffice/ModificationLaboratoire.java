        labo.setId_laboratoire(id);
        labo.setNom_labo(NomLabo.getText());
        labo.setEmail(EmailLabo.getText());
        labo.setResponsable(RespoLabo.getText());
        if(gestLabo.updateLaboratoire(labo.getId_laboratoire(), labo.getNom_labo(), labo.getEmail(), labo.getResponsable()))
        JOptionPane.showMessageDialog(null, "Modification réalisé avec succès ");
        else
        JOptionPane.showMessageDialog(null, "Erreur");
        Actualiser();
        Table(TableLabo);
        Table1();

